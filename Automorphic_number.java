public class Automorphic_number {
    public static void main(String args[])
    {
        int i=4;
        int j=i*i;
        if(j%10==i)
        {
            System.out.println("number is automorphic");
    }
    else
    {
        System.out.println("number is not automorphic");
    }
    }
    
}
